"""Learn to write and run a Python script that executes multiple commands in sequence."""
# ============================================================
# Exercise 03: Your First Script
# ============================================================
#
# A "script" is a file full of Python commands that run in order,
# from top to bottom. Instead of typing one command at a time
# in interactive mode, you write all your commands in a file
# and run them all at once.
#
# HOW TO RUN THIS FILE:
#   1. Open your terminal
#   2. Navigate to this folder (use cd command)
#   3. Type: python exercise.py
#   4. Press Enter
#
# Python will read this file from top to bottom and execute
# every line that is not a comment (comments start with #).
# ============================================================

# Step 1: Print a greeting.
print("Welcome to your first Python script!")

# Step 2: Do some math and print the result.
print("Let me calculate something for you...")
print("If you have 24 hours in a day and 7 days in a week:")
print("That is", 24 * 7, "hours in a week.")

# Step 3: Print a blank line for readability.
print()

# Step 4: Print a closing message.
print("This script ran from top to bottom.")
print("Every line executed in order.")
print("That is how all Python scripts work.")

# ============================================================
# KEY CONCEPT:
#
# Lines starting with # are "comments". Python ignores them.
# Comments are notes for humans reading the code.
# They explain what the code does and why.
#
# Everything else is code that Python executes.
# ============================================================
