"""Learn to display text and numbers on the screen using the print() function."""
# ============================================================
# Exercise 02: Hello World
# ============================================================
#
# print() is the most basic command in Python.
# It displays text on the screen.
#
# The text inside the quotes is called a "string".
# A string is just a piece of text.
# ============================================================

# This line displays the text "Hello, world!" on the screen.
# The quotes tell Python this is text, not a command.
print("Hello, world!")

# You can print anything inside quotes.
print("My name is Python and I do what you tell me.")

# You can print numbers without quotes.
print(42)

# You can print math results.
print(7 + 3)

# You can print multiple things separated by commas.
# Python adds a space between them automatically.
print("The answer is", 6 * 7)

# Empty print() just makes a blank line.
print()

# You can use single quotes or double quotes. Both work.
print('Single quotes work too.')
print("Double quotes also work.")

