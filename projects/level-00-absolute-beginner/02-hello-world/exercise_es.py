#
# print() es el comando más básico de Python.
# Muestra texto en la pantalla.
#
# El texto dentro de las comillas se llama "string" (cadena de texto).
# Un string es simplemente un pedazo de texto.
# ============================================================

# Esta línea muestra el texto "¡Hola, mundo!" en la pantalla.
# Las comillas le dicen a Python que esto es texto, no un comando.
print("¡Hola, mundo!")

# Puedes imprimir cualquier cosa dentro de comillas.
print("Mi nombre es Python y hago lo que me digas.")

# Puedes imprimir números sin comillas.
print(42)

# Puedes imprimir resultados de operaciones matemáticas.
print(7 + 3)

# Puedes imprimir varias cosas separadas por comas.
# Python agrega un espacio entre ellas automáticamente.
print("La respuesta es", 6 * 7)

# Un print() vacío solo hace una línea en blanco.
print()

# Puedes usar comillas simples o comillas dobles. Ambas funcionan.
print('Las comillas simples también funcionan.')
print("Las comillas dobles también funcionan.")

