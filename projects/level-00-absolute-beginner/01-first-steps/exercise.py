# This file is just for reference. The real exercise happens
# in your terminal with Python's interactive mode.
#
# WHAT TO DO:
#
# 1. Open your terminal (Command Prompt, PowerShell, or Terminal app)
#
# 2. Type this and press Enter:
#        python
#
#    You should see something like:
#        Python 3.12.0 (or whatever version you installed)
#        >>>
#
#    The >>> is called the "prompt". It means Python is waiting
#    for you to type something.
#
# 3. Type this and press Enter:
#        2 + 2
#
#    Python should respond with:
#        4
#
#    Congratulations. You just told a computer to do math.
#
# 4. Try a few more:
#        10 - 3
#        5 * 6
#        100 / 4
#
#    The * symbol means multiply. The / symbol means divide.
#
# 5. Try this:
#        print("Hello, I am learning Python!")
#
#    Python should print your message back to you.
#
# 6. When you are done, type this to leave Python:
#        exit()
#
# WHAT JUST HAPPENED:
#
# You opened Python in "interactive mode" â€” a place where you
# can type one command at a time and immediately see the result.
# This is like a calculator that also understands text and logic.
#
# You will not always use interactive mode. Soon you will write
# commands in a file and run the whole file at once. But interactive
# mode is great for experimenting.
# ============================================================

# This part runs if you execute this file directly.
# It just shows you the same things you tried interactively.

