#
# Este archivo es solo de referencia. El verdadero ejercicio
# se hace en tu terminal con el modo interactivo de Python.
#
# QUÉ HACER:
#
# 1. Abre tu terminal (Símbolo del sistema, PowerShell, o Terminal)
#
# 2. Escribe esto y presiona Enter:
#        python
#
#    Deberías ver algo como:
#        Python 3.12.0 (o la versión que hayas instalado)
#        >>>
#
#    El >>> se llama "prompt". Significa que Python está esperando
#    a que escribas algo.
#
# 3. Escribe esto y presiona Enter:
#        2 + 2
#
#    Python debería responder con:
#        4
#
#    Felicidades. Acabas de decirle a una computadora que haga matemáticas.
#
# 4. Prueba algunos más:
#        10 - 3
#        5 * 6
#        100 / 4
#
#    El símbolo * significa multiplicar. El símbolo / significa dividir.
#
# 5. Prueba esto:
#        print("¡Hola, estoy aprendiendo Python!")
#
#    Python debería mostrarte tu mensaje.
#
# 6. Cuando termines, escribe esto para salir de Python:
#        exit()
#
# QUÉ ACABA DE PASAR:
#
# Abriste Python en "modo interactivo" — un lugar donde puedes
# escribir un comando a la vez y ver el resultado inmediatamente.
# Es como una calculadora que también entiende texto y lógica.
#
# No siempre vas a usar el modo interactivo. Pronto escribirás
# comandos en un archivo y ejecutarás todo el archivo de una vez.
# Pero el modo interactivo es genial para experimentar.
# ============================================================

